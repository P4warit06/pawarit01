package Function.math;

public class MathFunc059 {
//.Create 4 basics function in MathFuncYYY.java (use 'public static' for create).
//function  name "add" function with 2 parameter (type int) and return.
    public static int add(int a, int b) {
        return a + b;


    }
//    function  name "subtract" function with 2 parameter (type int) and return.
    public static int subtract(int a, int b) {
        return a - b;


    }
//    function  name "multiply" function with 2 parameter (type int) and return.
    public static double multiply(double a, double b) {
        return a * b;


    }
//    function  name  "divide" function  with 2 parameter (type double) and return.
    public static double divide(double a, double b) {
        return a / b;

    }
}
